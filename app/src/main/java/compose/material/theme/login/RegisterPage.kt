@Composable
private fun GradientButton(
    gradientColors: List<Color>,
    cornerRadius: Dp,
    nameButton: String,
) {

    androidx.compose.material3.Button(
        modifier = Modifier
            .fillMaxWidth().absolutePadding(10.dp, 0.dp, 10.dp, 0.dp),
        onClick = {
